package Praktikum.sesi5;

public class Deret {
    public static void main(String[] args) {
         // Perulangan untuk menampilkan deret angka dari 1 hingga 5
         for (int i = 1; i <= 5; i++) {
            // Menampilkan iterasi dan angka saat ini
            System.out.println("Iterasi ke-" + i + ": Angka " + i);
        }
    }
}
